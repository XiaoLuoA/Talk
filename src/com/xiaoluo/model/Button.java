package com.xiaoluo.model;

public class Button {

	private java.lang.Integer id;
	private java.lang.Integer menuId;
	private java.lang.String btnName;
	private java.lang.String methodName;
	private java.lang.String className;
	public Button setId(java.lang.Integer id) {
		this.id = id;
		return this;
	}

	public java.lang.Integer getId() {
		return this.id;
	}

	public Button setMenuId(java.lang.Integer menuId) {
		this.menuId = menuId;
		return this;
	}

	public java.lang.Integer getMenuId() {
		return this.menuId;
	}

	public Button setBtnName(java.lang.String btnName) {
		this.btnName = btnName;
		return this;
	}

	public java.lang.String getBtnName() {
		return this.btnName;
	}

	public Button setMethodName(java.lang.String methodName) {
		this.methodName = methodName;
		return this;
	}

	public java.lang.String getMethodName() {
		return this.methodName;
	}

	public Button setClassName(java.lang.String className) {
		this.className = className;
		return this;
	}

	public java.lang.String getClassName() {
		return this.className;
	}

}
